    private void cboFuenteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboFuenteItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            String fuente = (String) cboFuente.getSelectedItem();
                if (fuente.equals(("Tahoma"))) {
                    lblMensaje.setFont(new java.awt.Font(fuente, 0, 14));
                } else if (fuente.equals(("Verdana"))) {
                    lblMensaje.setFont(new java.awt.Font(fuente, 0, 14));
                } else if (fuente.equals(("MS Gothic"))) {
                    lblMensaje.setFont(new java.awt.Font(fuente, 0, 14));
                } else if (fuente.equals(("Courier New"))) {
                    lblMensaje.setFont(new java.awt.Font(fuente, 0, 14));
                } 
        }
    }//GEN-LAST:event_cboFuenteItemStateChanged

