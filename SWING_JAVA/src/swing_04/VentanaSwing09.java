    private void cboContinenteItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboContinenteItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            String continente = (String) cboContinente.getSelectedItem();
            if (continente.equals(("EUROPA"))) {
                llenarLista("EUROPA");
            } else if (continente.equals(("AMERICA SUR"))) {
                llenarLista("AMERICA SUR");
            } else if (continente.equals(("AFRICA"))) {
                llenarLista("AFRICA");
            } else if (continente.equals(("OCEANIA"))) {
                llenarLista("OCEANIA");
            }
        }
    }//GEN-LAST:event_cboContinenteItemStateChanged

    public void llenarLista(String continente) {

        switch (continente) {
            case "EUROPA":
                dlm.clear();
                for (int i = 0; i < europa.length; i++) {
                    dlm.addElement(europa[i]);
                }
                break;
            case "AMERICA SUR":
                dlm.clear();
                for (int i = 0; i < americasur.length; i++) {
                    dlm.addElement(americasur[i]);
                }
                break;
            case "AFRICA":
                dlm.clear();
                for (int i = 0; i < africa.length; i++) {
                    dlm.addElement(africa[i]);
                }
                break;
            case "OCEANIA":
                dlm.clear();
                for (int i = 0; i < oceania.length; i++) {
                    dlm.addElement(oceania[i]);
                }
                break;
        }

    }

