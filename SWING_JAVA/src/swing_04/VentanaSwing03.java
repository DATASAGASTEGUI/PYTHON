    private void cboCiudadItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboCiudadItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            String ciudad = (String) cboCiudad.getSelectedItem();
            if (!ciudad.equals("CIUDAD")) {
                if (ciudad.equals(("SEVILLA"))) {
                    lblMensaje.setText("CAPITAL DE LA CA DE ANDALUCIA");
                } else if (ciudad.equals(("BILBAO"))) {
                    lblMensaje.setText("CAPITAL DE LA CA PAIS VASCO");
                } else if (ciudad.equals(("MADRID"))) {
                    lblMensaje.setText("CAPITAL DE LA CA DE MADRID");
                } else if (ciudad.equals(("VALENCIA"))) {
                    lblMensaje.setText("CAPITAL DE LA CA DE VALENCIA");
                } else if (ciudad.equals(("BARCELONA"))) {
                    lblMensaje.setText("CAPITAL DE LA CA DE CATALUÃ‘A");
                }
            } else {
                lblMensaje.setText("MENSAJE");
                JOptionPane.showMessageDialog(null, "DEBE SELECCIONAR UNA CIUDAD", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_cboCiudadItemStateChanged

    private void botRecuperarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botRecuperarActionPerformed
        String s = "";
        for (int i = 0; i < cboCiudad.getItemCount(); i++) {
            String ciudad = (String) cboCiudad.getItemAt(i);
            if(!ciudad.equals("CIUDAD")) {
               s = s + ciudad + "-";
            }
        }
        lblMensaje.setText(s);
    }//GEN-LAST:event_botRecuperarActionPerformed

    private void botBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botBuscarActionPerformed
        String ciudadBuscar = txtBuscar.getText();
        boolean bandera = false;
        for(int i=0; i<cboCiudad.getItemCount(); i++) {
            String ciudad = (String) cboCiudad.getItemAt(i);
            if(!ciudad.equals("CIUDAD")) {
                if(ciudadBuscar.equalsIgnoreCase(ciudad)) {
                   bandera = true;
                   break;
                }
            }
        }
        lblMensaje.setText(bandera ? "SI EXISTE":"NO EXISTE");
        
        if(bandera == false) {
            cboCiudad.addItem(ciudadBuscar);
        }
        
    }//GEN-LAST:event_botBuscarActionPerformed

